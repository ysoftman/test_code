"""
    01 (1)
and 10 (2)
------
    00 (0)
"""
print(1 & 2)

"""
    01 (1)
or  10 (2)
------
    11 (3)
"""
print(1 | 2)


"""
     01 (1)
xor  10 (2)
------
     11 (3)
"""
print(1 ^ 2)


"""
not operator
integer 에선 ~x=(-x)-1
~2=(-2)-1=-3
또는
= ~10
= -(10+1)
= -(11)
= -3

~10 = (-10)-1)=-11
또는
= ~1010
= -(1010+1)
= -(1011)
= -11
"""
print(~2)
print(~~2)
print(~10)
print(~~10)


"""
shift<<3  10 (2)
-----
       10000 (16)
"""
print(2 << 3)


"""
shift>>2  10000 (16)
------
            100 (4)
"""
print(16 >> 2)
