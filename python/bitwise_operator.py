"""
    01 (1)
and 10 (2)
------
    00 (0)
"""

print(1 & 2)

"""
    01 (1)
or  10 (2)
------
    11 (3)
"""
print(1 | 2)


"""
     01 (1)
xor  10 (2)
------
     11 (3)
"""
print(1 ^ 2)


"""
not operator
integer 에선 ~x=(-x)-1 로 간단하게 계산

~2=(-2)-1=-3
또는
= ~10
= -(10+1)
= -(11)
= -3
또는
= ~00000010
= 11111101(1의보수) => 음수이기 때문에 1111 부분을 0으로 만들기위해
=> 00000010(1의보수)+1(2의보수) => 00000011 에 음수 표시 => 1(부호비트)0000011 => -3

~10=(-10)-1=-11
또는
= -(1010+1)
= -(1011)
= -11
또는
= ~00001010
= 11110101(1의보수) => 음수이기 때문에 1111 부분을 0으로 만들기위해
=> 00001010(1의보수)+1(2의보수) => 00001011 에 음수 표시 => 1(부호비트)0001011 => -11
"""

print("---")
print("~2:", ~2)
print("~~2:", ~~2)
print("~10:", ~10)
print("~~10:", ~~10)
print("---")

"""
shift<<3  10 (2)
-----
       10000 (16)
"""
print(2 << 3)


"""
shift>>2  10000 (16)
------
            100 (4)
"""
print(16 >> 2)
